Tutorial ini untuk install q4os linux 32bit di laptop HP mini 311.

Persiapan
1. Laptop HP mini 311 32bit RAM 1GB
2. File ISO q4os //peringatan: beda versi kemungkinan akan beda alur. Versi saat pembuatan tutorial ini adalah versi 3.12 (Centaurus)

Proses
1. Burn ISO ke flash disk dengan balenaEtcher
2. Boot Laptop dengan klik Esc saat laptop booting
3. Klik F9
4. Pilih nama flashdisk
5. Proses instalasi Linux
6. Saat partition, jika menghendaki dualboot atau tidak menghapus data di hdd, pilih 4. Manual
7. Proses instalasi selesai
8. Pilih profil minimalis
9. Install aplikasi yang dibutuhkan
10. sudo apt update
11. sudo apt install kde-plasma-desktop
12. sudo apt install 

